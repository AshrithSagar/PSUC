// Preprocessor directives.
#include <stdio.h>

int main() // Start main.
{ // Start.

	// Variable initialisations.
	float C, F;
	
	// Ask user for input.
	printf("Enter temperature in Fahrenheit: \n");
	scanf("%f", &F);

	C = (5.0/9) * (F - 32);

	// Display output.
	printf("The given temperature in Centigrade is %f C.\n", C);

	printf("\nMy name is Ashrith Sagar Yedlapalli.\n");

} // End.
